Matrix4x4 Multiply(const Matrix4x4& m1, const Matrix4x4& m2) {
	Matrix4x4 result3;
	for (int m = 0; m < 4; m++) {
		for (int n = 0; n < 4; n++) {
			result3.m[m][n] = 0;
			for (int mn = 0; mn < 4; mn++) {
				result3.m[m][n] += m1.m[m][mn] * m2.m[mn][n];
			}
		}
	}
	return result3;
}

