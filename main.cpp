Matrix4x4 MakePerspectiveFovMatrix(float fovY, float aspectRatio, float nearClip, float farClip) {

	Matrix4x4 tositoei;
	tositoei.m[0][0] = (1 / aspectRatio) * cosf(fovY / 2.0f) / sinf(fovY / 2.0f);
	tositoei.m[0][1] = 0;
	tositoei.m[0][2] = 0;
	tositoei.m[0][3] = 0;
	tositoei.m[1][0] = 0;
	tositoei.m[1][1] = cosf(fovY / 2.0f) / sinf(fovY / 2.0f);
	tositoei.m[1][2] = 0;
	tositoei.m[1][3] = 0;
	tositoei.m[2][0] = 0;
	tositoei.m[2][1] = 0;
	tositoei.m[2][2] = farClip / (farClip - nearClip);
	tositoei.m[2][3] = 1;
	tositoei.m[3][0] = 0;
	tositoei.m[3][1] = 0;
	tositoei.m[3][2] = (-nearClip * farClip) / (farClip - nearClip);
	tositoei.m[3][3] = 0;
	return tositoei;
}

Matrix4x4 MakeViewportMatrix(float left, float top, float width, float height, float minDepth, float maxDepth) {

	Matrix4x4 viewport;
	viewport.m[0][0] = width / 2.0f;
	viewport.m[0][1] = 0;
	viewport.m[0][2] = 0;
	viewport.m[0][3] = 0;
	viewport.m[1][0] = 0;
	viewport.m[1][1] = (-height / 2.0f);
	viewport.m[1][2] = 0;
	viewport.m[1][3] = 0;
	viewport.m[2][0] = 0;
	viewport.m[2][1] = 0;
	viewport.m[2][2] = maxDepth - minDepth;
	viewport.m[2][3] = 0;
	viewport.m[3][0] = left + (width / 2.0f);
	viewport.m[3][1] = top + (height / 2.0f);
	viewport.m[3][2] = minDepth;
	viewport.m[3][3] = 1;
	return viewport;

}
Vector3 Transform(const Vector3& vector, const Matrix4x4& matrix) {
	Vector3 result;
	result.x = vector.x * matrix.m[0][0] + vector.y * matrix.m[1][0] + vector.z * matrix.m[2][0] + 1.0f * matrix.m[3][0];
	result.y = vector.x * matrix.m[0][1] + vector.y * matrix.m[1][1] + vector.z * matrix.m[2][1] + 1.0f * matrix.m[3][1];
	result.z = vector.x * matrix.m[0][2] + vector.y * matrix.m[1][2] + vector.z * matrix.m[2][2] + 1.0f * matrix.m[3][2];
	float w = vector.x * matrix.m[0][3] + vector.y * matrix.m[1][3] + vector.z * matrix.m[2][3] + 1.0f * matrix.m[3][3];
	assert(w != 0.0f);
	result.x /= w;
	result.y /= w;
	result.z /= w;
	return result;
}
