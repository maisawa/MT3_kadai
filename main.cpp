		Matrix4x4 rotateYMatrix = MakeRotateYMatrix(rotate.y+=0.05f);//Ry　回転y
		Matrix4x4 rotateZMatrix = MakeRotateZMatrix(rotate.z);//Rz　回転z
		Matrix4x4 rotateXYMatrix = Multiply(rotateXMatrix,rotateYMatrix);//R=RxRyRz;
		Matrix4x4 rotateXYandZMatrix = Multiply(rotateXYMatrix,rotateZMatrix);

		Matrix4x4 translateMatrix = MakeTranslateMatrix(translate);//T　平行移動

		Matrix4x4 worldMatrix = MakeAffineMatrix({ 1.0f,1.0f,1.0f }, rotate, translate);//W　アフィン行列
		//W=SRT;
		
		Matrix4x4 cameraMatrix = MakeAffineMatrix({ 1.0f,1.0f,1.0f }, { 0.0f,0.0f,0.0f }, cameraposition);

		Matrix4x4 viewMatrix = Inverse(cameraMatrix);

		Matrix4x4 projectionMatrix = MakePerspectiveFovMatrix(0.45f,float(kWindowWidth)/float (kWindowHeight),0.1f,100.0f);

		Matrix4x4 worldViewProjectionMatrix = Multiply(worldMatrix,Multiply(viewMatrix,projectionMatrix));

		Matrix4x4 viewportMatrix = MakeViewportMatrix(0,0,float(kWindowWidth),float(kWindowHeight),0.0f,1.0f);

		Vector3 screenVertices[3];
		
		for (uint32_t i = 0; i < 3; i++) {

			Vector3 ndcVertex = Transform(kLocalVertices[i], worldViewProjectionMatrix);
			screenVertices[i] = Transform(ndcVertex, viewportMatrix);
			
		}
		//----------------------------------------------------------
		Novice::DrawTriangle(int(screenVertices[0].x),int(screenVertices[0].y),
			int(screenVertices[1].x),int(screenVertices[1].y),
			int(screenVertices[2].x),int(screenVertices[2].y),RED,kFillModeSolid);
